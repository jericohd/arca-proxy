"""Regression guard for the cache-quality eval + the L2 safety contract.

Numbers are reported in benchmarks/EVAL_METRICS.md (generated by
`python benchmarks/eval_metrics.py`). This file guards two things:
  1. the eval dataset's integrity, and
  2. the SAFETY CONTRACT: the retrieve-then-verify pipeline (cosine + polarity
     guard) produces ZERO false hits on the adversarial set, while recovering
     meaningful recall — the property that makes semantic caching safe.
"""
from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pytest

from arca.embeddings import embed
from arca.semantic_guard import is_safe_match

V2 = Path(__file__).parent / "data" / "eval_pairs_v2.jsonl"
GUARD_COSINE_THRESHOLD = 0.90  # recommended operating point (see EVAL_METRICS.md)


def _load() -> list[dict]:
    return [
        json.loads(line)
        for line in V2.read_text().splitlines()
        if line.strip() and not line.startswith("//")
    ]


def test_v2_dataset_wellformed():
    pairs = _load()
    assert len(pairs) >= 60, f"eval set too small to be meaningful: {len(pairs)}"
    hits = [p for p in pairs if p["expect"] == "hit"]
    misses = [p for p in pairs if p["expect"] == "miss"]
    assert len(hits) >= 25 and len(misses) >= 25, "set must be roughly balanced"
    assert all(set(p) == {"a", "b", "expect"} for p in pairs), "unexpected keys"
    assert all(p["expect"] in ("hit", "miss") for p in pairs)
    assert all(p["a"].strip() and p["b"].strip() for p in pairs), "empty prompt"
    keys = {(p["a"], p["b"]) for p in pairs}
    assert len(keys) == len(pairs), "duplicate pair in eval set"


async def _scored() -> list[tuple[str, float, str, str]]:
    pairs = _load()
    uniq = sorted({p["a"] for p in pairs} | {p["b"] for p in pairs})
    vecs = {s: await embed(s) for s in uniq}
    return [(p["expect"], float(np.dot(vecs[p["a"]], vecs[p["b"]])), p["a"], p["b"]) for p in pairs]


async def test_guard_yields_zero_false_hits():
    """SAFETY CONTRACT: cosine + polarity guard never serves a wrong cached answer
    on the adversarial set. A false hit here means a real user gets a wrong answer."""
    scored = await _scored()
    false_hits = [
        (a, b, c) for e, c, a, b in scored
        if e == "miss" and c >= GUARD_COSINE_THRESHOLD and is_safe_match(a, b)
    ]
    assert not false_hits, f"polarity guard leaked {len(false_hits)} false hit(s): {false_hits}"


async def test_guard_recovers_meaningful_recall():
    """The guard must not gut recall to buy precision: the two-stage pipeline
    catches materially more paraphrases than the old safe cosine-only point (≈0%)."""
    scored = await _scored()
    n_hit = sum(1 for e, *_ in scored if e == "hit")
    caught = sum(
        1 for e, c, a, b in scored
        if e == "hit" and c >= GUARD_COSINE_THRESHOLD and is_safe_match(a, b)
    )
    recall = caught / n_hit
    assert recall >= 0.45, f"recall regressed to {recall:.0%} (expected ≥45%)"


def test_guard_rejects_known_polarity_flips():
    """Spot-check the failure modes that motivated the guard (EVAL_METRICS.md)."""
    assert not is_safe_match("how to encode base64 in python", "how to decode base64 in python")
    assert not is_safe_match("convert a list to a tuple in python", "convert a tuple to a list in python")
    assert not is_safe_match("how to enable ssl in nginx", "how to disable ssl in nginx")
    assert is_safe_match("what is a python decorator", "explain python decorators")
