"""Regression guard for the cache-quality eval + the L2 safety contract.

Numbers are reported in benchmarks/EVAL_METRICS.md (generated by
`python benchmarks/eval_metrics.py`). This file guards:
  1. integrity of all eval sets (dev + held-out),
  2. the polarity guard's GENERALIZATION on a frozen HELD-OUT set — this is the
     honest signal (the guard was tuned on v2, so v2 numbers would overfit).
"""
from __future__ import annotations

import json
from pathlib import Path

import numpy as np

from arca.embeddings import embed
from arca.semantic_guard import is_safe_match

DATA = Path(__file__).parent / "data"
DEV = DATA / "eval_pairs_v2.jsonl"
HELDOUT = DATA / "eval_pairs_v4_heldout.jsonl"  # fresh set the guard never saw
ALL_SETS = [DEV, DATA / "eval_pairs_v3_heldout.jsonl", HELDOUT]
GUARD_THR = 0.90  # operating point (see EVAL_METRICS.md)


def _load(path: Path) -> list[dict]:
    return [
        json.loads(line)
        for line in path.read_text().splitlines()
        if line.strip() and not line.startswith("//")
    ]


def test_eval_sets_wellformed():
    for path in ALL_SETS:
        pairs = _load(path)
        assert len(pairs) >= 30, f"{path.name} too small: {len(pairs)}"
        assert all(set(p) == {"a", "b", "expect"} for p in pairs), f"{path.name} bad keys"
        assert all(p["expect"] in ("hit", "miss") for p in pairs), f"{path.name} bad label"
        assert all(p["a"].strip() and p["b"].strip() for p in pairs), f"{path.name} empty"
        keys = {(p["a"], p["b"]) for p in pairs}
        assert len(keys) == len(pairs), f"{path.name} has duplicate pairs"


async def _scored(path: Path) -> list[tuple[str, float, str, str]]:
    pairs = _load(path)
    uniq = sorted({p["a"] for p in pairs} | {p["b"] for p in pairs})
    vecs = {s: await embed(s) for s in uniq}
    return [(p["expect"], float(np.dot(vecs[p["a"]], vecs[p["b"]])), p["a"], p["b"]) for p in pairs]


def _confusion(scored):
    tp = sum(1 for e, c, a, b in scored if e == "hit" and c >= GUARD_THR and is_safe_match(a, b))
    fp = sum(1 for e, c, a, b in scored if e == "miss" and c >= GUARD_THR and is_safe_match(a, b))
    n_hit = sum(1 for e, *_ in scored if e == "hit")
    return tp, fp, n_hit


async def test_guard_generalizes_on_heldout():
    """HONEST safety signal: on a fresh held-out set the guard was never tuned on,
    cosine + guard keeps precision high (the overfit hand-list scored only 79%
    held-out; general morphological rules recover it). This is the number that
    matters — not the tuned-set 100%."""
    tp, fp, n_hit = _confusion(await _scored(HELDOUT))
    precision = tp / (tp + fp) if (tp + fp) else 1.0
    assert precision >= 0.85, f"held-out precision regressed to {precision:.0%} (overfitting?)"
    assert tp / n_hit >= 0.45, f"held-out recall regressed to {tp / n_hit:.0%}"


async def test_guard_beats_cosine_alone_on_heldout():
    """The guard must add real precision over naive cosine on held-out data."""
    scored = await _scored(HELDOUT)
    fp_cos = sum(1 for e, c, a, b in scored if e == "miss" and c >= GUARD_THR)
    fp_grd = sum(1 for e, c, a, b in scored if e == "miss" and c >= GUARD_THR and is_safe_match(a, b))
    assert fp_grd < fp_cos, "guard did not reduce false hits vs cosine alone"


def test_guard_rejects_general_polarity_flips():
    """Spot-check GENERAL failure modes (not copied from any eval file)."""
    assert not is_safe_match("how to zip a folder", "how to unzip a folder")          # morphology
    assert not is_safe_match("how to encode base64", "how to decode base64")          # opposite prefix
    assert not is_safe_match("convert celsius to fahrenheit", "convert fahrenheit to celsius")  # direction
    assert not is_safe_match("enable logging", "disable logging")                     # opposite prefix
    assert is_safe_match("what is a python decorator", "explain python decorators")   # true paraphrase
