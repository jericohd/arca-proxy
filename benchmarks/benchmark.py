"""Arca reproducible benchmark. Runs fully offline against a mocked upstream.

    python benchmarks/benchmark.py

What it measures (and what it does not):
  1. Proxy overhead and cache-hit latency through the full ASGI stack with the
     Anthropic upstream replaced by an instant in-process mock. Numbers exclude
     real network/model time by design: a cache hit avoids the entire upstream
     round trip, so the relevant question is how much latency Arca itself adds.
  2. Semantic-threshold quality on the committed 18-pair eval set
     (tests/data/eval_pairs_v1.jsonl): false positives and paraphrase recall
     at several cosine thresholds.
  3. Cache hit rates on a synthetic three-phase workload (cold, exact repeat,
     paraphrase) and the estimated cost avoided at published per-MTok rates.
     The workload shape and per-request token counts are stated assumptions,
     not measurements of real traffic.

Writes benchmarks/RESULTS.md and prints it to stdout.
"""
from __future__ import annotations

import asyncio
import json
import platform
import statistics
import tempfile
import time
from pathlib import Path

import httpx
import numpy as np
from httpx import ASGITransport, AsyncClient

REPO = Path(__file__).resolve().parent.parent
EVAL_PATH = REPO / "tests" / "data" / "eval_pairs_v1.jsonl"
RESULTS_PATH = Path(__file__).resolve().parent / "RESULTS.md"

ASSUMED_INPUT_TOKENS = 1500
ASSUMED_OUTPUT_TOKENS = 350
PRICED_MODEL = "claude-sonnet-4-6"


def _sse_body(input_tokens: int, output_tokens: int, text: str) -> bytes:
    events = [
        ("message_start", {"type": "message_start", "message": {
            "id": "msg_bench", "type": "message", "role": "assistant",
            "model": PRICED_MODEL, "content": [],
            "usage": {"input_tokens": input_tokens, "output_tokens": 1}}}),
        ("content_block_start", {"type": "content_block_start", "index": 0,
                                 "content_block": {"type": "text", "text": ""}}),
        ("content_block_delta", {"type": "content_block_delta", "index": 0,
                                 "delta": {"type": "text_delta", "text": text}}),
        ("content_block_stop", {"type": "content_block_stop", "index": 0}),
        ("message_delta", {"type": "message_delta", "delta": {"stop_reason": "end_turn"},
                           "usage": {"output_tokens": output_tokens}}),
        ("message_stop", {"type": "message_stop"}),
    ]
    return b"".join(
        f"event: {name}\ndata: {json.dumps(payload)}\n\n".encode() for name, payload in events
    )


class MockAnthropic(httpx.AsyncBaseTransport):
    """Instant upstream: returns a canned SSE stream for every request."""

    def __init__(self) -> None:
        self.calls = 0

    async def handle_async_request(self, request: httpx.Request) -> httpx.Response:
        self.calls += 1
        return httpx.Response(
            200,
            content=_sse_body(ASSUMED_INPUT_TOKENS, ASSUMED_OUTPUT_TOKENS,
                              "benchmark canned response"),
            headers={"content-type": "text/event-stream"},
        )


def _pct(values: list[float], q: float) -> float:
    s = sorted(values)
    return s[min(len(s) - 1, max(0, int(round(q * len(s))) - 1))]


def _load_pairs() -> list[dict]:
    return [json.loads(line) for line in EVAL_PATH.read_text().splitlines() if line.strip()]


def _body(prompt: str) -> bytes:
    return json.dumps({
        "model": PRICED_MODEL,
        "max_tokens": 1024,
        "stream": True,
        "messages": [{"role": "user", "content": prompt}],
    }).encode()


async def run() -> str:
    import os

    tmp = tempfile.mkdtemp(prefix="arca-bench-")
    os.environ["ARCA_HOME"] = tmp

    from arca.cache import SIMILARITY_THRESHOLD
    from arca.embeddings import EMBEDDING_MODEL, embed, warm_up
    from arca.fallback import SQLiteFallback
    from arca.observability import calculate_cost
    from arca.proxy import app

    t0 = time.monotonic()
    await asyncio.to_thread(warm_up)
    model_load_s = time.monotonic() - t0

    mock = MockAnthropic()
    upstream = httpx.AsyncClient(transport=mock)
    app.state.client = upstream
    store = SQLiteFallback()
    await store.start()
    app.state.sqlite_fallback = store

    pairs = _load_pairs()
    hit_pairs = [p for p in pairs if p["expect"] == "hit"]
    miss_pairs = [p for p in pairs if p["expect"] == "miss"]

    lines: list[str] = []
    lines.append("# Arca benchmark results\n")
    lines.append(f"Generated by `python benchmarks/benchmark.py` on "
                 f"{platform.platform()} / Python {platform.python_version()} / CPU only.\n")
    lines.append(f"Embedding model load (cold start, cached weights): {model_load_s:.2f} s\n")

    # ----- 1. Latency through the full ASGI stack ---------------------------
    cold_prompts = [f"benchmark unique prompt number {i}: explain topic {i}" for i in range(40)]
    miss_ms: list[float] = []
    l1_ms: list[float] = []

    async with AsyncClient(transport=ASGITransport(app=app), base_url="http://arca") as client:
        for prompt in cold_prompts:
            t = time.perf_counter()
            r = await client.post("/v1/messages", content=_body(prompt),
                                  headers={"x-api-key": "sk-bench", "content-type": "application/json"})
            assert r.status_code == 200
            miss_ms.append((time.perf_counter() - t) * 1000)
        await asyncio.sleep(0.3)  # let fire-and-forget write-backs settle

        upstream_before = mock.calls
        for prompt in cold_prompts:
            t = time.perf_counter()
            r = await client.post("/v1/messages", content=_body(prompt),
                                  headers={"x-api-key": "sk-bench", "content-type": "application/json"})
            assert r.status_code == 200
            l1_ms.append((time.perf_counter() - t) * 1000)
        l1_hits = len(cold_prompts) - (mock.calls - upstream_before)

        # ----- 2. Semantic (local L2) phase: seed a, query paraphrase b ------
        sem_ms: list[float] = []
        for p in hit_pairs:
            r = await client.post("/v1/messages", content=_body(p["a"]),
                                  headers={"x-api-key": "sk-bench", "content-type": "application/json"})
            assert r.status_code == 200
        await asyncio.sleep(0.3)
        upstream_before = mock.calls
        for p in hit_pairs:
            t = time.perf_counter()
            r = await client.post("/v1/messages", content=_body(p["b"]),
                                  headers={"x-api-key": "sk-bench", "content-type": "application/json"})
            assert r.status_code == 200
            sem_ms.append((time.perf_counter() - t) * 1000)
        sem_hits = len(hit_pairs) - (mock.calls - upstream_before)

        # negative controls: paraphrase-unrelated queries must NOT hit
        upstream_before = mock.calls
        for p in miss_pairs:
            r = await client.post("/v1/messages", content=_body(p["b"]),
                                  headers={"x-api-key": "sk-bench", "content-type": "application/json"})
            assert r.status_code == 200
        false_hits = len(miss_pairs) - (mock.calls - upstream_before)

    lines.append("## 1. Latency (full ASGI request path, instant mocked upstream)\n")
    lines.append("Hit latencies are end-to-end through Arca. Miss latency here is Arca's")
    lines.append("added overhead only (canonicalize + embed + local L2 search + tee); a real")
    lines.append("miss additionally pays the normal Anthropic round trip.\n")
    lines.append("| Path | p50 | p95 | n |")
    lines.append("|---|---|---|---|")
    lines.append(f"| Miss overhead (canonicalize + embed + L2 search) | {_pct(miss_ms, 0.5):.1f} ms | {_pct(miss_ms, 0.95):.1f} ms | {len(miss_ms)} |")
    lines.append(f"| L1 hit (exact repeat) | {_pct(l1_ms, 0.5):.1f} ms | {_pct(l1_ms, 0.95):.1f} ms | {len(l1_ms)} |")
    lines.append(f"| Local semantic hit (SQLite brute-force cosine) | {_pct(sem_ms, 0.5):.1f} ms | {_pct(sem_ms, 0.95):.1f} ms | {len(sem_ms)} |")
    lines.append("")
    lines.append(f"L1 hit rate on exact repeats: {l1_hits}/{len(cold_prompts)}. ")

    # ----- 3. Threshold quality on the committed eval set -------------------
    lines.append("## 2. Semantic threshold quality (18-pair eval set)\n")
    lines.append(f"Cosine similarity between each pair, embedded with {EMBEDDING_MODEL}.")
    lines.append("`hit` pairs are paraphrases that should match; `miss` pairs are near-miss")
    lines.append("lookalikes (including negation) that must not.\n")
    scores = []
    for p in pairs:
        va = await embed(p["a"])
        vb = await embed(p["b"])
        scores.append((p, float(np.dot(va, vb))))
    lines.append("| Threshold | Paraphrase recall | False positives |")
    lines.append("|---|---|---|")
    for th in (0.90, 0.92, 0.95):
        recall = sum(1 for p, s in scores if p["expect"] == "hit" and s >= th)
        fp = sum(1 for p, s in scores if p["expect"] == "miss" and s >= th)
        lines.append(f"| {th:.2f} | {recall}/{len(hit_pairs)} | {fp}/{len(miss_pairs)} |")
    lines.append("")
    lines.append(f"End-to-end through the proxy at the default threshold ({SIMILARITY_THRESHOLD}): "
                 f"{sem_hits}/{len(hit_pairs)} paraphrase hits, "
                 f"{false_hits}/{len(miss_pairs)} false hits on negative controls.\n")

    # ----- 4. Cost model ------------------------------------------------------
    total_requests = len(cold_prompts) * 2 + len(hit_pairs) * 2 + len(miss_pairs)
    total_hits = l1_hits + sem_hits
    per_call = calculate_cost(PRICED_MODEL, ASSUMED_INPUT_TOKENS, ASSUMED_OUTPUT_TOKENS)
    saved = total_hits * per_call
    spent = (total_requests - total_hits) * per_call
    lines.append("## 3. Estimated cost avoided (model-based, stated assumptions)\n")
    lines.append(f"Assumptions: every request is {ASSUMED_INPUT_TOKENS} input / "
                 f"{ASSUMED_OUTPUT_TOKENS} output tokens on {PRICED_MODEL} "
                 f"(3.00 / 15.00 USD per MTok). These are assumptions, not measurements.\n")
    lines.append(f"- Workload: {total_requests} requests, {total_hits} served from cache "
                 f"({100 * total_hits / total_requests:.0f}% hit rate in this synthetic workload)")
    lines.append(f"- Cost per uncached call: ${per_call:.6f}")
    lines.append(f"- API spend avoided: ${saved:.4f} vs ${spent + saved:.4f} without caching "
                 f"({100 * saved / (spent + saved):.0f}%)")
    lines.append("")
    lines.append("The hit rate above is a property of this synthetic workload (every cold")
    lines.append("prompt repeated once, plus paraphrase pairs), not a claim about real Claude")
    lines.append("Code traffic. Real multi-turn agent sessions repeat far less; see the")
    lines.append("Limitations section of the README.\n")

    await store.stop()
    await upstream.aclose()
    return "\n".join(lines)


if __name__ == "__main__":
    report = asyncio.run(run())
    RESULTS_PATH.write_text(report)
    print(report)
    print(f"\n[written to {RESULTS_PATH}]")
